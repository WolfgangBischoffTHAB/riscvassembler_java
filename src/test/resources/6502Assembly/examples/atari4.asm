VerticalBlank   STA WSYNC
                INX
                CPX #37
                BNE VerticalBlank

                ; Scanlines
                LDX #192
Main            
                STA WSYNC
                
                LDA Screen_1_PFColor-1,X
                STA COLUPF
                LDA Screen_1_BKColor-1,X
                STA COLUBK
                
                LDA Screen_1_PF0-1,X
                STA PF0
                LDA Screen_1_PF1-1,X
                STA PF1
                LDA Screen_1_PF2-1,X
                STA PF2
                
                DEX
                BNE Main
                
                ; Clear Playfield
                LDA #0
                STA COLUPF
                STA COLUBK

EndScreen       LDA #%00000010
                STA VBLANK

                ; Overscan
                LDX #30
Overscan        STA WSYNC
                DEX
                BNE Overscan
                JMP StartOfFrame

